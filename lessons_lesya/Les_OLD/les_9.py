n = int(input())
list = []

def calculate():
    if n // n:
        result_1 =  n // n
        list.append(result_1)
        if  n % 5 == 0:
            result_2 = n // 5
            list.append(result_2)
            if  n // 1:
                result_3 = n // 1
                list.append(result_3)
calculate()            
print(list)
   
    

    
